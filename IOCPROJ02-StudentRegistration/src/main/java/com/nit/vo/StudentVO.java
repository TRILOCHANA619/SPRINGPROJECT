package com.nit.vo;

public class StudentVO {
	private String sname;
	private String sdept;
	private String mathMark;
	private String scienceMark;
	private String csMark;
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public String getSdept() {
		return sdept;
	}
	public void setSdept(String sdept) {
		this.sdept = sdept;
	}
	public String getMathMark() {
		return mathMark;
	}
	public void setMathMark(String mathMark) {
		this.mathMark = mathMark;
	}
	public String getScienceMark() {
		return scienceMark;
	}
	public void setScienceMark(String scienceMark) {
		this.scienceMark = scienceMark;
	}
	public String getCsMark() {
		return csMark;
	}
	public void setCsMark(String csMark) {
		this.csMark = csMark;
	}
}
